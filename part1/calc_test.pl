#!/usr/bin/env perl
use strict;
use warnings;

my $foo = 4;
my $bar = 2;
my $result;

$result = $foo + $bar;
print "$foo + $bar = $result\n";
$result = $foo - $bar;
print "$foo - $bar = $result\n";
$result = $foo * $bar;
print "$foo * $bar = $result\n";
$result = $foo / $bar;
print "$foo - $bar = $result\n";
$result = $foo % $bar;
print "$foo % $bar = $result\n";
$result = $foo ** $bar;
print "$foo ** $bar = $result\n";

